// limitations under the License.

// Code generated by hack/update-pre-release-sail-images.sh. DO NOT EDIT.

package install

import "github.com/istio-ecosystem/sail-operator/pkg/config"

func init() {
	config.Config.ImageDigests = map[string]config.IstioImageConfig{
HEADER

for version in "${VERSIONS[@]}"; do
    istiod="${ISTIOD["${version}"]:-}"
    proxy="${PROXY["${version}"]:-}"
    cni="${CNI["${version}"]:-}"
    ztunnel="${ZTUNNEL["${version}"]:-}"

    cat >> "${TARGET}" << ENTRY
		"${version}": {
			IstiodImage:  "${istiod}",
			ProxyImage:   "${proxy}",
			CNIImage:     "${cni}",
			ZTunnelImage: "${ztunnel}",
		},
ENTRY
done

cat >> "${TARGET}" << 'FOOTER'
	}
}
FOOTER

echo "> Generated ${TARGET}"
echo ""
echo "Verify with:"
echo "  go build ./..."
echo "  go test ./pkg/operator/controller/gatewayclass/ -run TestSailLibraryImageDigestsAreDownstream"
